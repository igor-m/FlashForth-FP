#include <stddef.h>
#include <math.h>

float Cfadd(float x, float y)
{
    return x + y;
}

float Cfsub(float x, float y)
{
    return x - y;
}

float Cfmul(float x, float y)
{
    return x * y;
}

float Cfdiv(float x, float y)
{
    return x / y;
}

float Cfsqrt(float x)
{
    return sqrtf(x);
}

float Cfsin(float x)
{
    return sinf(x);
}

float Cfcos(float x)
{
    return cosf(x);
}

float Cftan(float x)
{
    return tanf(x);
}

float Cfexp(float x)
{
    return expf(x);
}

float Cfpow(float x, float y)
{
    return powf(x, y);
}

float Cflog10(float x)
{
    return log10f(x);
}

float Cflog(float x)
{
    return logf(x);
}

float Cfatan(float x)
{
    return atanf(x);
}

float Cfasin(float x)
{
    return asinf(x);
}

float Cfacos(float x)
{
    return acosf(x);
}

float Cfatan2(float x, float y)
{
    return atan2f(x, y);
}

float Cftanh(float x)
{
    return tanhf(x);
}

float Cfsinh(float x)
{
    return sinhf(x);
}

float Cfcosh(float x)
{
    return coshf(x);
}







